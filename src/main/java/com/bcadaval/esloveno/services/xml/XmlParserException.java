package com.bcadaval.esloveno.services.xml;

public class XmlParserException extends Exception {

	private static final long serialVersionUID = -6455408509342582463L;

	public XmlParserException() {
		super();
	}

	public XmlParserException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public XmlParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public XmlParserException(String message) {
		super(message);
	}

	public XmlParserException(Throwable cause) {
		super(cause);
	}

}
