package com.bcadaval.esloveno.services.xml;

public class ScrapException extends Exception {

	private static final long serialVersionUID = -1911724997954618547L;

	public ScrapException() {
		super();
	}

	public ScrapException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ScrapException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScrapException(String message) {
		super(message);
	}

	public ScrapException(Throwable cause) {
		super(cause);
	}

}
